# iterate through list
colors = ['blue','yellow','red','green']

for color in colors:
    print(color)

#tuple
ponit = (1,2,3)
for value in ponit:
    print(value)

# iterate through dictionary

ages = {'kevin':59,'bob':40,'kayla':21}

#print names
for key in ages:
    print(key)

#print name with age
for key, value in ages.items():
    print(key,value)

# iterate through string
for letter in 'my-string':
    print(letter)

